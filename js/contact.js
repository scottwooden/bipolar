$(document).ready(function() {
  setTimeout(function() {
	   initMap();
  }, 2000);

  $('.checkbox').click(function() {
    $(this).toggleClass('active');
  });

  $('#submit-form-btn').click(function() {

    $(this).html('Sending...').addClass('disabled');

    let name    = $('#user-name').val();
    let title   = $('#user-title').val();
    let client  = $('#user-client').val();
    let agency  = $('#user-agency').val();
    let contact = $('#user-contact').val();
    let work = $('#user-work').val();
    let projectType = stringProjectTypes();

    $.post("../contact.php", { 
        name: name,
        contact: contact,
        client: client,
        agency: agency,
        title: title,
        work: work,
        type: projectType
      },
      function(data) {
        console.log(data);
        if (data == "success") {
            $('#submit-form-btn').html('Thanks! We will be in touch').addClass('disabled');
            $('.done-close').removeClass('hide');
        }
    });

  });

});

function stringProjectTypes() {
    let projectTypes = [];

    $('.checkbox').each(function(target, index) {
        if($(this).hasClass('active')) {
            let value = $(this).children('.checkbox-boxLabel').html();
            projectTypes.push(value);
        }
    });

    return projectTypes.toString();
}

